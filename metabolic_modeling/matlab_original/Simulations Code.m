    biomass_ph(((i*3)-(3-j)),1)=Conditions(j,1);
    flux_ph(((i*3)-(3-j)),1) = FBA_photo_nonzero(i,j);

  end
end    

flux_ph = cell2mat(flux_ph);

photo_tbl = table(subs_ph,biomass_ph,flux_ph);

photo_hm = heatmap(photo_tbl,'biomass_ph','subs_ph','ColorVariable','flux_ph','ColorMethod','mean')


