model=iDunsR_v14;
Duns_genes = fastaread('Dunaliella_work.fasta')
table = table()
for i = 1:length(Duns_genes)
    index=strmatch(Duns_genes(i).Header,model.genes,'exact');
    if ~isempty(index)
